# Assignment number 1: Mad Libs Game

# Getting user input
adjective = input("Enter an adjective: ")
verb = input("Enter a verb: ")
noun = input("Enter a noun: ")
place = input("Enter a place: ")

# Creating the Mad Libs story
story = f"One day, a {adjective} {noun} decided to {verb} at the {place}. \
Everyone was shocked and started laughing."

# Printing the final story
print("\nHere is your Mad Libs story:", story)

